				<h1>History</h1>
				<p class="top-margin-20">In 1969, as most of the great movie palaces were closing down, the 175th St. Theatre was saved from demolition by the Rev. Frederick J. Eikerenkoetter II. Better known as Reverend Ike, Eikerenkoetter bought the building for more than a half-million dollars and used it as the headquarters for his United Church Science of Living Institute. The historic theatre was exquisitely restored by the church and is also used for concerts. Reverend Ike died on July 28, 2009.</p>
				<p class="top-margin-20">Performers since 2007 include Vampire Weekend, Eddie Vedder, Sonic Youth, Bob Dylan, The Smashing Pumpkins, Beck, Sigur R&#243;s, Jackson Browne, Bj&#246;rk, Iggy and the Stooges, Modest Mouse, The Black Crowes, and The Arcade Fire. Sir Simon Rattle appeared at the theatre conducting the Berlin Philharmonic in Stravinsky's The Rite of Spring. In 2008 a performance of Leonard Bernstein's Mass was given as part of the celebration of the 90th anniversary of that composer's birth. In addition, recitals, classes and lectures have also been presented at the theater.</p>
				<p class="top-margin-20">While the church continues to own the theater, the recently inaugurated rock concerts have been produced by JAB II LLC.</p>
				
